#include <stdio.h>

int main() {
    // Elabore um programa em C para imprimir os 30 primeiros números naturais pares.
    // Use a estrutura for para gerar a sequência de termos.

    for (int i = 0; i < 60; i += 2) { // Incrementa i em 2 a cada iteração
        printf("%d ", i);
    }

    return 0;
}
